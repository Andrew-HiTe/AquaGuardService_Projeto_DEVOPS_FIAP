
CREATE TABLE SENSOR (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    LOCALIZACAO VARCHAR2(255) NOT NULL,
    TIPO VARCHAR2(50) NOT NULL,
    LIMITE_CONSUMO NUMBER(10, 2),
    ATIVO NUMBER(1) DEFAULT 1 NOT NULL,
    CONSTRAINT SENSOR_PK PRIMARY KEY (ID)
);

CREATE TABLE LEITURA (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    SENSOR_ID NUMBER NOT NULL,
    VALOR NUMBER(10, 2) NOT NULL,
    "TIMESTAMP" TIMESTAMP NOT NULL,
    CONSTRAINT LEITURA_PK PRIMARY KEY (ID),
    CONSTRAINT LEITURA_SENSOR_FK FOREIGN KEY (SENSOR_ID) REFERENCES SENSOR (ID)
);

CREATE TABLE ALERTA (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    SENSOR_ID NUMBER NOT NULL,
    MENSAGEM VARCHAR2(500) NOT NULL,
    "TIMESTAMP" TIMESTAMP NOT NULL,
    RESOLVIDO NUMBER(1) DEFAULT 0 NOT NULL,
    CONSTRAINT ALERTA_PK PRIMARY KEY (ID),
    CONSTRAINT ALERTA_SENSOR_FK FOREIGN KEY (SENSOR_ID) REFERENCES SENSOR (ID)
);


CREATE SEQUENCE SENSOR_SEQ START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE LEITURA_SEQ START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE ALERTA_SEQ START WITH 1 INCREMENT BY 1;


